   I-  Introduction.<br>
      <p>  Dans le monde de l’informatique, l’utilisation des bases de données est devenue un standard vue la taille continuellement grandissante des données à sauvegarder. Nous nous intéressons ici à la gestion d’une base de donnée d’un supermarché contenant des informations sur le personnel, le stock et ventes. Pour ce faire, nous avons recueilli les informations essentielles.<br></p>

          
II-	Recueil des informations.<br>

1-	Le personnel.<br>
       <p> Le supermarché possède son personnel ayant chacun un nom, un salaire, une adresse et occupant un poste précis. Nous désirons gérer le personnel en répondant aux préoccupations suivantes :<br>
-	Enregistrer un nouvel employer dans la base de données. <br>
-	Supprimer un employer.<br>
-	Connaître les informations sur un employer.<br>
-	Avoir la liste de tous les employers.<br></p>

      2- Les fournisseurs.<br>
 <p>  Le supermarché se ravitaille auprès de ses fournisseurs dont on connaît pour chacun d’eux le nom et l’adresse. En ce qui concerne ces fournisseurs, nous désirons :<br>
-	Lister les fournisseurs de l’entreprise.<br>
     -    Insérer un nouveau fournisseur <br>
     -    supprimer un fournisseur de la liste	 <br></p>

3-Les produits <br>

 <p> Un fournisseur livre un certain nombre de produits dont on connaît le nom et le prix de livraison. Concernant ces produits, nous effectuerons les opérations suivantes :<br>
-	Insérer un nouveau produit,<br>
-	Supprimer des produits,<br>
-	Lister l’ensemble des produits du magasin.<br></p>

     4- Les ventes.<br>

  <p> Pour vendre ses produits, le supermarché enregistre des informations telles que le nom du client, le nom du produit vendu, sa quantité, son prix de vente et la date. On pourra éventuellement effectuer une remise (exprimée en pourcentage). Ainsi nous aimerions :      <br>
-	insérer une nouvelle vente, <br>
-	supprimer une vente.<br>
-	Connaître le client ayant effectué la plus grande recette durant une période.<br>
-	Lister les ventes effectuées pendant une période.<br>
-	Connaître la marchandise la plus vendue durant une période.<br>
-	Connaître le montant des ventes effectuées durant une période.<br></p>

  

   5- Le stock.<br>

<p>Le ravitaillement du supermarché se fait en stocks. C’est un ensemble homogène de produits ayant la même date de fabrication. pour chaque stock, on connaît le nom du produit , la quantité, le prix unitaire, le prix d’achat total et sa date de péremption. Pour leur gestion efficace, il sera judicieux que nous puissions :<br>
-	Insérer un nouveau stock,<br>
-	Afficher la table de stocks.<br>
-	Savoir les produits en rupture de stock,<br>
-	Savoir les stocks dont la date de péremption approche,<br>
-	Supprimer un stock suivant certains critères,<br>
-	Afficher les stocks périmés du supermarché,<br></p>

6- Les clients.<br>

   <p>	Le super marché a des clients parmi lesquels certains sont particuliers, vu leurs achats effectués : ils ont chacun un nom et une adresse. Pour les gérer, on doit pouvoir :<br>
-	Insérer un nouveau client,<br>
-	Lister l’ensemble des clients,<br>
-	Supprimer un client de la liste des meilleurs clients pour une certaine raison.<br></p>

7- Les commandes.<br>

  <p>	Un client peut effectuer une commande dont il précise le produit, sa quantité et la date de livraison désirée. Ainsi à un moment donné nous pouvons savoir :<br>
-	Lister les commandes effectuées par les clients,<br>
-	Insérer une nouvelle commande,<br>
-	Supprimer une commande de la liste pour un certain nombre de raisons.<br></p>

     8-  La livraison.<br>

<p>Quand le supermarché veut de ravitailler en un produit donné, on contacte le fournisseur correspondant suite à laquelle ce dernier effectue la livraison.<br>
 Nous pouvons donc : <br>
-	Lister les livraisons de l'entreprise,<br>
-	Connaître les fournisseurs d’un produit,<br>
-	Insérer une nouvelle livraison,<br>
-	Supprimer une livraison de la lister. <br></p>

  
